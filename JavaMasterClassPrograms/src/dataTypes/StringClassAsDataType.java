package dataTypes;

public class StringClassAsDataType {

	public static void main(String[] args) {
	
		String myString = "Hello";
		System.out.println(myString);
		
		int myInt = 50;
		myString = myString + myInt;
		System.out.println(myString);
		
		double myDouble = 20.30;
		myString = myString+myDouble;
		System.out.println(myString);

	}

}
