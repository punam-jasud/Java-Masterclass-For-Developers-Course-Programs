package section3.dataTypes;

public class CharBoolean {

	public static void main(String[] args) {

		char myChar = 'D';
		char myUnicodeCharacter = '\u0044';
		System.out.println(myChar);
		System.out.println(myUnicodeCharacter);

		char myCopyRight = '\u00A9';
		System.out.println(myCopyRight);

		boolean myTrueBooleanValue = true;
		boolean myFalseBooleanValue = false;

		System.out.println(myTrueBooleanValue);
		System.out.println(myFalseBooleanValue);

	}

}
